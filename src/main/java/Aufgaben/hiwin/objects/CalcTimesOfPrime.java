package Aufgaben.hiwin.objects;

public class CalcTimesOfPrime {
    private final long min;
    private final long max;


    public CalcTimesOfPrime(long min, long max) {
        this.min = min;
        this.max = max;
    }

    public long getMin() {
        return min;
    }

    public long getMax() {
        return max;
    }

}
